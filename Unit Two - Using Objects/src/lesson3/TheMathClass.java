package lesson3;

public class TheMathClass {
    public static void main(String[] args) {
        int radius=3;
        double area=Math.PI*Math.pow(radius,2); //radius to the power of 2. like radius x radius

        System.out.println(area);

        int x = -56;
        double y=-67.72372;

        System.out.println(x); // prints x
        System.out.println(Math.abs(x)); // prints the absolute value of x

        System.out.println(y); //prints y
        System.out.println(Math.abs(y)); // prints the absolute value of y


        System.out.println(Math.sqrt(2)); //square root 
    }
}
